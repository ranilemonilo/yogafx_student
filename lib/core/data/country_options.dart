CountryOption? findCountryByName(String? name) {
  if (name == null || name.trim().isEmpty) return null;
  final normalized = name.trim().toLowerCase();
  for (final option in countryOptions) {
    if (option.name.toLowerCase() == normalized) {
      return option;
    }
  }
  return null;
}

CountryOption? findCountryByDialCode(String? dialCode) {
  if (dialCode == null || dialCode.trim().isEmpty) return null;
  final normalized = dialCode.trim();
  for (final option in countryOptions) {
    if (option.dialCode == normalized) {
      return option;
    }
  }
  return null;
}

({CountryOption? country, String localNumber}) splitPhoneNumber(String? value) {
  if (value == null || value.trim().isEmpty) {
    return (country: null, localNumber: '');
  }

  final normalized = value.trim().replaceAll(' ', '');
  final sorted = [...countryOptions]
    ..sort((a, b) => b.dialCode.length.compareTo(a.dialCode.length));

  for (final option in sorted) {
    if (normalized.startsWith(option.dialCode)) {
      return (
        country: option,
        localNumber: normalized.substring(option.dialCode.length),
      );
    }
  }

  return (country: null, localNumber: normalized);
}
